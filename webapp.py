def predict_image(image_bytes):
    # 将上传的图像字节转换为 PIL Image 对象
    img = Image.open(BytesIO(image_bytes))

    trans_ops = transforms.Compose([
        transforms.Resize(256),
        transforms.CenterCrop(224),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406],
                             std=[0.229, 0.224, 0.225])
    ])

    images = trans_ops(img).view(-1, 3, 224, 224)

    # 使用模型进行预测
    outputs = model(images)

    # 处理预测结果，返回最可能的类别
    #most_probable_class = tf.argmax(predictions, axis=1).numpy()[0]

    predictions = outputs.topk(5, dim=1)
    # 一个将类别索引映射到实际类别名称的字典
    # 根据模型和类别进行调整
    class_map = {0: '苹果健康', 1: '苹果黑星病一般', 2: '苹果黑星病严重', 3: '苹果灰斑病'}
    return class_map[predictions.numpy()[0]]


